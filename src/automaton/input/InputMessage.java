package automaton.input;

import java.util.Map;

/**
 * Универсальное входное сообщение для вероятностного автомата.
 * Позволяет передавать любые внешние данные в унифицированном виде.
 */
public interface InputMessage {

    /** Сырые данные сообщения (например, полный текст или идентификатор события). */
    String getRaw();

    /** Структурированные атрибуты, извлечённые из сообщения. */
    Map<String, Object> getAttributes();
}
